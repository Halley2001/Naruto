            int eleccionPersonaje = personajes - 1;
            int enemigo = random.nextInt(5);

            System.out.println("\n" + historiaCamino);
            System.out.println(rojo + "\nHa aparecido un enemigo.");
            System.out.println("Te has topado con " + nombresEnemigos[enemigo] + ". ¡Prepárate!" + reset);
            
            //ASCII Enemigos
